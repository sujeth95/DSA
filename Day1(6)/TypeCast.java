import java.util.*;

public class TypeCast {
    public static void main(String args[]){
        // Scanner src = new Scanner (System.in);
        float a = 24.12f;
        int b = (int)a;
        System.out.println(b);

        char ch = 'b';
        int number = (int)ch;
        System.out.println(number);
    }
}